n1, n2 = eval(input("សូមបញ្ចូលចំនួនគត់ពីរផ្សេងគ្នា :"))
gcd = 1 # ផ្ដើមតម្លៃ gcd = 1
k = 2  # តម្លៃអាចជា gcd
while k <= n1 and k <= n2:
    if n1 % k == 0 and n2 % k == 0:
        gcd = k
    k += 1 # តម្លៃបន្ទាប់អាចជា gcd
print("តួចែកធំបំផុតរវាង ", n1,"និង ", n2, "គឺ ", gcd)
# បន្ទាប់ពីរង្វិលជុំនេះហើយយើងនឹងរកឃើយ gcd រវាងតម្លៃ n1 និង n2

