year = eval(input("បញ្ចូលឆ្នាំណាមួយ: "))
zodiacYear = year % 12
if zodiacYear == 0:
    print("ឆ្នាំវក តំណាងសត្វ ស្វា")
elif zodiacYear == 1:
    print("ឆ្នាំរកា តំណាងសត្វ មាន់")
elif zodiacYear == 2:
    print("ឆ្នាំច តំណាងសត្វ ឆ្កែ")
elif zodiacYear == 3:
    print("ឆ្នាំកុរ តំណាងសត្វ ជ្រូក")
elif zodiacYear == 4:
    print("ឆ្នាំជូត តំណាងសត្វ កណ្ដុរ")
elif zodiacYear == 5:
    print("ឆ្នាំឆ្លូវ តំណាងសត្វ គោ")
elif zodiacYear == 6:
    print("ឆ្នាំខាល តំណាងសត្វ ខ្លា")
elif zodiacYear == 7:
    print("ឆ្នាំថោះ តំណាងសត្វ ទន្សាយ")
elif zodiacYear == 8:
    print("ឆ្នាំរោង តំណាងសត្វ នាគ")
elif zodiacYear == 9:
    print("ឆ្នាំម្សាញ់ តំណាងសត្វ ពស់")
elif zodiacYear == 10:
    print("ឆ្នាំមមី តំណាងសត្វ សេះ")
else :
    print("ឆ្នាំមមែ តំណាងសត្វ ពពែរ")


