integerNumber = 123 # ផ្ដើមតម្លៃចំនួនគត់ ឱ្យអថេរ integerNumber
floatNumber = 1.23  # ផ្ដើមតម្លៃចំនួនទស្សភាគ ឱ្យអថេរ floatNumber
#ប្រភេទទិន្នន័យដែលបំប្លែងដោយស្វ័យប្រវត្តិ
newNumber = integerNumber + floatNumber

# បង្ហាញតម្លៃថ្មីក្រោយការគណនា និងប្រភេទទិន្នន័យរបស់វា
print("លទ្ធផលនៃការគណនា:", newNumber)
print("ប្រភេទទិន្នន័យ:", type(newNumber))