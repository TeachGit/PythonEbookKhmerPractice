# សម្រាប់បញ្ចូលតម្លៃលេខទាំងបី
number1 = eval(input("សូមបញ្ចូលលេខទីមួយ: "))
number2 = eval(input("សូមបញ្ចូលលេខទីពីរ: "))
number3 = eval(input("សូមបញ្ចូលលេខទីបី: "))
average = (number1 + number2 + number3) / 3
# បង្ហាញតម្លៃមធ្យមរបស់ចំនួនទាំងបី
print("តម្លៃមធ្យមរបស់", number1, number2, number3, " គឺ ", average)

